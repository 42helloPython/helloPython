import java.util.*;
class _8393 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        int sum = 0;
        for (int i = 1; i < t + 1; i++ ) {
            sum += i;
        }
        System.out.println(sum);
    }
}